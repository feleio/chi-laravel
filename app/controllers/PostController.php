class PostController extends \BaseController {

	public function index()
	{
		$posts = Post::with('image')->orderBy('created_at','desc')->get();
		return View::make('posts.index')->with('posts',$posts);
	}

	/**
	 * Show the form for creating a new resource.
	 *
	 * @return Response
